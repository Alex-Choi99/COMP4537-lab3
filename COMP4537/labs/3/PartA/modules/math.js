/**
 * Adds two numbers.
 * @param {*} x | The first number
 * @param {*} y | The second number
 * @returns | The sum of the two numbers
 */
function add(x, y) {
    return x + y;
}

/**
 * Subtracts two numbers.
 * @param {*} x | The first number
 * @param {*} y | The second number
 * @returns | The difference of the two numbers
 */
function subtract(x, y) {
    return x - y;
}

module.exports = { add, subtract };
